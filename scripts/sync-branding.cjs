const fs = require('node:fs');
const path = require('node:path');

const repoRoot = path.resolve(__dirname, '..');
const canonicalDir = path.join(repoRoot, 'branding');
const canonicalMarkPath = path.join(canonicalDir, 'my-own-suite-mark.png');
const canonicalFaviconsDir = path.join(canonicalDir, 'favicons');
const canonicalStylesPath = path.join(canonicalDir, 'styles', 'mos.css');
const homepageCustomCssPath = path.join(repoRoot, 'apps', 'homepage', 'config', 'custom.css');

const destinations = {
  sitePublicBrand: path.join(repoRoot, 'site', 'public', 'brand'),
  sitePublicRoot: path.join(repoRoot, 'site', 'public'),
  siteSourceBrand: path.join(repoRoot, 'site', 'src', 'assets', 'brand'),
  siteGeneratedCss: path.join(repoRoot, 'site', 'src', 'generated', 'branding', 'mos.css'),
  suitePublicBrand: path.join(repoRoot, 'apps', 'suite-manager', 'frontend', 'public', 'brand'),
  suiteStylesCss: path.join(repoRoot, 'apps', 'suite-manager', 'frontend', 'src', 'styles', 'mos.css'),
};

function assertExists(targetPath) {
  if (!fs.existsSync(targetPath)) {
    throw new Error(`Branding source not found: ${targetPath}`);
  }
}

function ensureDir(targetPath) {
  fs.mkdirSync(targetPath, { recursive: true });
}

function copyFile(sourcePath, destinationPath) {
  ensureDir(path.dirname(destinationPath));
  fs.copyFileSync(sourcePath, destinationPath);
}

function hexToRgb(hex) {
  const normalized = hex.trim().replace(/^#/, '');
  const expanded = normalized.length === 3
    ? normalized.split('').map((part) => `${part}${part}`).join('')
    : normalized;

  if (!/^[0-9a-fA-F]{6}$/.test(expanded)) {
    throw new Error(`Unsupported hex color: ${hex}`);
  }

  return {
    b: parseInt(expanded.slice(4, 6), 16),
    g: parseInt(expanded.slice(2, 4), 16),
    r: parseInt(expanded.slice(0, 2), 16),
  };
}

function rgbToTriplet({ r, g, b }) {
  return `${r} ${g} ${b}`;
}

function mixRgb(a, b, ratio) {
  return {
    b: Math.round(a.b + (b.b - a.b) * ratio),
    g: Math.round(a.g + (b.g - a.g) * ratio),
    r: Math.round(a.r + (b.r - a.r) * ratio),
  };
}

function extractBrandHex(source, tokenName) {
  const pattern = new RegExp(`${tokenName}:\\s*(#[0-9a-fA-F]{3,6})\\s*;`);
  const match = source.match(pattern);
  if (!match) {
    throw new Error(`Unable to find ${tokenName} in branding stylesheet.`);
  }
  return match[1];
}

function writeGeneratedCss(sourcePath, destinationPath) {
  ensureDir(path.dirname(destinationPath));
  const source = fs.readFileSync(sourcePath, 'utf8');
  const output = `/*\n * Generated by scripts/sync-branding.cjs from branding/styles/mos.css.\n * Do not edit this file directly.\n */\n\n${source}`;
  fs.writeFileSync(destinationPath, output);
}

function syncFavicons(destinationDir) {
  ensureDir(destinationDir);
  for (const entry of fs.readdirSync(canonicalFaviconsDir, { withFileTypes: true })) {
    if (!entry.isFile()) {
      continue;
    }

    copyFile(
      path.join(canonicalFaviconsDir, entry.name),
      path.join(destinationDir, entry.name),
    );
  }
}

function syncHomepageTheme(sourcePath, destinationPath) {
  const source = fs.readFileSync(sourcePath, 'utf8');
  const accent = hexToRgb(extractBrandHex(source, '--mos-color-accent'));
  const accentStrong = hexToRgb(extractBrandHex(source, '--mos-color-accent-strong'));
  const bg = hexToRgb(extractBrandHex(source, '--mos-color-bg'));
  const bgSoft = hexToRgb(extractBrandHex(source, '--mos-color-bg-soft'));
  const text = hexToRgb(extractBrandHex(source, '--mos-color-text'));
  const white = { r: 255, g: 255, b: 255 };

  const palette = {
    50: mixRgb(accent, white, 0.88),
    100: mixRgb(accent, white, 0.76),
    200: mixRgb(accent, white, 0.6),
    300: mixRgb(accent, white, 0.38),
    400: mixRgb(accent, white, 0.18),
    500: accent,
    600: mixRgb(accentStrong, bg, 0.18),
    700: mixRgb(accentStrong, bg, 0.38),
    800: bgSoft,
    900: bg,
  };

  const generatedBlock = `/* sync-branding:homepage:start */
:root {
  --mos-bg: rgb(var(--bg-color));
  --mos-bg-soft: color-mix(in srgb, rgb(var(--bg-color)) 88%, rgb(var(--color-100)));
  --mos-surface: color-mix(in srgb, rgb(var(--color-100)) 42%, transparent);
  --mos-surface-strong: color-mix(in srgb, rgb(var(--color-100)) 58%, transparent);
  --mos-border: color-mix(in srgb, rgb(var(--color-300)) 28%, transparent);
  --mos-text: rgb(var(--color-800));
  --mos-muted: color-mix(in srgb, rgb(var(--color-700)) 78%, transparent);
  --mos-accent: rgb(var(--color-500));
  --mos-accent-soft: color-mix(in srgb, rgb(var(--color-400)) 18%, transparent);
  --mos-shadow: none;
}

html.dark {
  --mos-bg: rgb(var(--bg-color));
  --mos-bg-soft: color-mix(in srgb, rgb(var(--bg-color)) 84%, rgb(var(--color-900)));
  --mos-surface: color-mix(in srgb, rgb(var(--color-100)) 10%, transparent);
  --mos-surface-strong: color-mix(in srgb, rgb(var(--color-100)) 14%, transparent);
  --mos-border: color-mix(in srgb, rgb(var(--color-300)) 20%, transparent);
  --mos-text: rgb(var(--color-200));
  --mos-muted: color-mix(in srgb, rgb(var(--color-300)) 82%, transparent);
  --mos-accent: rgb(var(--color-300));
  --mos-accent-soft: color-mix(in srgb, rgb(var(--color-300)) 18%, transparent);
}

.theme-mos {
  --color-50: ${rgbToTriplet(palette[50])};
  --color-100: ${rgbToTriplet(palette[100])};
  --color-200: ${rgbToTriplet(palette[200])};
  --color-300: ${rgbToTriplet(palette[300])};
  --color-400: ${rgbToTriplet(palette[400])};
  --color-500: ${rgbToTriplet(palette[500])};
  --color-600: ${rgbToTriplet(palette[600])};
  --color-700: ${rgbToTriplet(palette[700])};
  --color-800: ${rgbToTriplet(palette[800])};
  --color-900: ${rgbToTriplet(palette[900])};
  --color-logo-start: ${rgbToTriplet(mixRgb(accent, white, 0.18))};
  --color-logo-stop: ${rgbToTriplet(accentStrong)};
}

.theme-mos .text-theme-800 {
  color: rgb(${rgbToTriplet(bgSoft)});
}

.theme-mos.dark .text-theme-800,
.theme-mos.dark .dark\\:text-theme-200:is(.dark *) {
  color: rgb(${rgbToTriplet(text)});
}
/* sync-branding:homepage:end */`;

  const destination = fs.readFileSync(destinationPath, 'utf8');
  const pattern = /\/\* sync-branding:homepage:start \*\/[\s\S]*?\/\* sync-branding:homepage:end \*\//;
  if (!pattern.test(destination)) {
    throw new Error('Homepage custom.css is missing sync-branding markers.');
  }

  fs.writeFileSync(destinationPath, destination.replace(pattern, generatedBlock));
}

function main() {
  assertExists(canonicalMarkPath);
  assertExists(canonicalFaviconsDir);
  assertExists(canonicalStylesPath);
  assertExists(homepageCustomCssPath);

  copyFile(canonicalMarkPath, path.join(destinations.sitePublicBrand, 'my-own-suite-mark.png'));
  copyFile(canonicalMarkPath, path.join(destinations.siteSourceBrand, 'my-own-suite-mark.png'));
  copyFile(canonicalMarkPath, path.join(destinations.suitePublicBrand, 'my-own-suite-mark.png'));

  syncFavicons(destinations.sitePublicBrand);
  syncFavicons(destinations.sitePublicRoot);
  syncFavicons(destinations.suitePublicBrand);

  writeGeneratedCss(canonicalStylesPath, destinations.siteGeneratedCss);
  writeGeneratedCss(canonicalStylesPath, destinations.suiteStylesCss);
  syncHomepageTheme(canonicalStylesPath, homepageCustomCssPath);

  process.stdout.write('Branding assets and styles synchronized.\n');
}

main();
